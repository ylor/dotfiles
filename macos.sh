#!/usr/bin/env bash
# inspired by https://mths.be/macos

# Close any open System Preferences panes, to prevent them from overriding
# settings we’re about to change
osascript -e 'tell application "System Preferences" to quit'

# Get the command line tools and accept the license, if not installed/accepted
if ! xcode-select -p &> /dev/null; then
  	xcode-select --install &> /dev/null
	sudo xcodebuild -license accept &> /dev/null
fi

# Install Homebrew if it's not already installed and install packages
if ! command -v brew; then
	ruby -e "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/master/install)" && brew update && brew upgrade && brew bundle && brew cleanup
	
	# Brew taps
	taps=(
	beeftornado/rmtree
	homebrew/cask-fonts
	)  
	for tap in "${taps[@]}"  
	do  
	    echo brew tap $tap
	done
	
	# Brew packages
	pkgs=(
	bash
	bat
	exa
	fd
	ffmpeg
	fish
	fzf
	git
	htop
	iperf3
	mas
	media-info
	mkvtoolnix
	neovim
	nnn
	rename
	ripgrep
	tmux
	trash
	tree
	)  
	for pkg in "${pkgs[@]}"  
	do  
	    echo brew install $pkg
	done
	
	# Brew casks
	casks=(
	1password
	appcleaner
	hazel
	hwsensors
	font-inter
	keyboardcleantools
	mkvtools
	mp4tools
	mpv
	omnidisksweeper
	superduper
	the-unarchiver
	visual-studio-code
	xld
	)  
	for cask in "${casks[@]}"  
	do  
	    echo brew cask install $cask
	done
	
	# mas
	apps=(John Harry Jake Scott Philis)  
	for app in "${apps[@]}"  
	do  
	    echo mas $app
	done  
fi

# Install Nix if it's not already been installed 
if ! command -v nix; then
	curl https://nixos.org/nix/install | sh
fi

# Make SFMono available to other apps
if [[ -d "/Applications/Utilities/Terminal.app/Contents/Resources/Fonts/" ]] && [[ ! -f "$HOME/Library/Fonts/SFMono-Regular.otf" ]]; then
	open "/Applications/Utilities/Terminal.app/Contents/Resources/Fonts/"SFMono*.otf
fi

# Prompt for hostname change
read -t 30 -rp "Current hostname is $(hostname). Would you like to change it?
Please confirm within 30 seconds. [Y/n]" changeHostname
case $changeHostname in
    [Yy][Ee][Ss]|[Yy])
        read -rp "Enter new hostname: " newHostname
        sudo scutil --set ComputerName "$newHostname"
        sudo scutil --set HostName "$newHostname"
        sudo scutil --set LocalHostName "$newHostname"
        sudo defaults write /Library/Preferences/SystemConfiguration/com.apple.smb.server NetBIOSName -string "$newHostname"
    ;;
    *)
    ;;
esac

###############################################################################
# General UI/UX                                                               #
###############################################################################

# Dark Mode
osascript -e 'tell application "System Events" to tell appearance preferences to set dark mode to true'

# Set standby delay to 24 hours (default is 1 hour)
#sudo pmset -a standbydelay 86400

# Disable the sound effects on boot
sudo nvram SystemAudioVolume=" "

# Set sidebar icon size to medium
# defaults write NSGlobalDomain NSTableViewDefaultSizeMode -int 2

# Increase window resize speed for Cocoa applications
# defaults write NSGlobalDomain NSWindowResizeTime -float 0.001

# Expand save panel by default
#defaults write NSGlobalDomain NSNavPanelExpandedStateForSaveMode -bool true
#defaults write NSGlobalDomain NSNavPanelExpandedStateForSaveMode2 -bool true

# Save to disk (not to iCloud) by default
#defaults write NSGlobalDomain NSDocumentSaveNewDocumentsToCloud -bool false

# Automatically quit printer app once the print jobs complete
defaults write com.apple.print.PrintingPrefs "Quit When Finished" -bool true

# Disable the “Are you sure you want to open this application?” dialog
defaults write com.apple.LaunchServices LSQuarantine -bool false

# Disable the crash reporter
defaults write com.apple.CrashReporter DialogType -string "none"

# Disable automatic capitalization as it’s annoying when typing code
#defaults write NSGlobalDomain NSAutomaticCapitalizationEnabled -bool false

# Disable smart dashes as they’re annoying when typing code
#defaults write NSGlobalDomain NSAutomaticDashSubstitutionEnabled -bool false

# Disable automatic period substitution as it’s annoying when typing code
#defaults write NSGlobalDomain NSAutomaticPeriodSubstitutionEnabled -bool false

# Disable smart quotes as they’re annoying when typing code
#defaults write NSGlobalDomain NSAutomaticQuoteSubstitutionEnabled -bool false

# Disable auto-correct
#defaults write NSGlobalDomain NSAutomaticSpellingCorrectionEnabled -bool false

###############################################################################
# Trackpad, mouse, keyboard, Bluetooth accessories, and input                 #
###############################################################################

